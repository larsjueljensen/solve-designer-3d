function MovingObject.GetTopNode ( hHitObject )
--------------------------------------------------------------------------------
        
    local hObject = hHitObject
    local hParent = object.getParent ( hObject )
    
    while ( hParent ~= nil ) do
        hObject = hParent
        hParent = object.getParent ( hObject )
    end
    
    return hObject
	
--------------------------------------------------------------------------------
end
