function WardrobeXmlReader.buildObjectURL ( sArticleNumber )
--------------------------------------------------------------------------------
    
    local sBaseURL = application.getCurrentUserAIVariable ( "DesignerPlugin_Main", "URLbase" )
    return sBaseURL.."3dobject/"..sArticleNumber..".3do"
    
--------------------------------------------------------------------------------
end
