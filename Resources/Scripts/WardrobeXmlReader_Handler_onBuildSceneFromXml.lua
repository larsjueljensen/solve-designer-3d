function WardrobeXmlReader.onBuildSceneFromXml ( sXml )
--------------------------------------------------------------------------------

    local hXml = xml.newInstance ( )
    xml.createFromString ( hXml, sXml )
    local hRootXml = xml.getRootElement ( hXml )    
    this.buildFromWardrobeElement( hRootXml );
	
--------------------------------------------------------------------------------
end
