function ASPMain.ReceiveSearchSeries ( hComponent)
--------------------------------------------------------------------------------
    for i = 0, 15 do
    
        hud.addListItem ( hud.getComponent ( this.getUser ( ), "controls.SeriesResult" ), "Serie :"..i )
    
    end

    --Show results.
    hud.callAction ( this.getUser ( ),"controls.ShowSeriesResults" )

	
--------------------------------------------------------------------------------
end
