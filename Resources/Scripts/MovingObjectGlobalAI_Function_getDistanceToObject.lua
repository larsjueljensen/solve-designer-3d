function MovingObjectGlobalAI.getDistanceToObject ( hObject, x, y, z, space )
--------------------------------------------------------------------------------

    local ox, oy, oz = object.getTranslation ( hObject, space )
    local dx, dy, dz = x - ox, y - oy, z - oz
    return math.vectorLength ( dx, dy, dz )
	
--------------------------------------------------------------------------------
end
