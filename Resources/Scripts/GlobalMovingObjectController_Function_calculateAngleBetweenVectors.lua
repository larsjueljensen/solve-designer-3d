function GlobalMovingObjectController.calculateAngleBetweenVectors ( ax, ay, bx, by )
--------------------------------------------------------------------------------

    return math.atan2 ( by, bx ) -  math.atan2 ( ay, ax )
    
--------------------------------------------------------------------------------
end
