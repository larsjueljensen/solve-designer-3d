--------------------------------------------------------------------------------
function WardrobeXmlBuilder.buildXYZ ( hElement, x, y, z )
--------------------------------------------------------------------------------

    xml.appendElementChild ( hElement, "x", string.format ( "%d", x ) )
    xml.appendElementChild ( hElement, "y", string.format ( "%d", y ) )
    xml.appendElementChild ( hElement, "z", string.format ( "%d", z ) )	
	
--------------------------------------------------------------------------------
end
