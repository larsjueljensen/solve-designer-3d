function MovingCollisionAI.isWardrobeObject ( hObject )
--------------------------------------------------------------------------------
	
    if ( hObject ~= nil ) then
        return 
            string.compare ( "WardrobeObject", object.getModelName ( hObject ) ) == 0
            or  string.compare ( "WardrobeGroup", object.getModelName ( hObject ) ) == 0
    end
    
    return false
    
--------------------------------------------------------------------------------
end
