function ASPMain.CheckHUD ( )
--------------------------------------------------------------------------------
	
    
    local hComponent = hud.getComponent ( this.getUser ( ), "main.ButtonFrame1" )
    
    log.message ( "hComponent: ", hComponent )
    
    this.TakeSnapshot ( )
    
    if hComponent then
    
    this.HideHUD ( )

    
    else
    
    log.message ( "HUD is hidden" )
    this.TakeSnapshot ( )
    
    end
    

	
--------------------------------------------------------------------------------
end
