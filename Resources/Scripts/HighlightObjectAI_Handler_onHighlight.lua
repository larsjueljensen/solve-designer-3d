function HighlightObjectAI.onHighlight ( bTurnHighlightOn )
--------------------------------------------------------------------------------
	
    if ( bTurnHighlightOn ) then
        this.highlightObject( this.getObject ( ), true )
    else
        this.highlightObject( this.getObject ( ), false )
    end
	
--------------------------------------------------------------------------------
end
