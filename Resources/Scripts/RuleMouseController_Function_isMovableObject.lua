function RuleMouseController.isMovableObject ( hObject )
--------------------------------------------------------------------------------

    return sensor.getCategoryBitAt ( hObject, 0, this.kSensorCategoryBitMovable ( ) )
	
--------------------------------------------------------------------------------
end
