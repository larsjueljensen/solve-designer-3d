function MovingObjectLocalAI.getWardrobeObject ( hObject )
--------------------------------------------------------------------------------

    local hResult = hObject
    
    while ( hResult ~= nil ) do
    
        if this.isHelperObject( hResult, "WardrobeObject" ) then
            return hResult
        end

        hResult = object.getParent ( hResult )

    end
        
--------------------------------------------------------------------------------
end
