function ASPCamera.onRotateBy ( nDeltaX, nDeltaY )
--------------------------------------------------------------------------------
	
    if nDeltaX >= 24 then
        nDeltaX = 20
    end
    
    if nDeltaX <= -24 then
        nDeltaX = -20
    end

	this.nDstAngleX ( this.nDstAngleX ( ) - nDeltaX * 90 )
    this.nDstAngleY ( this.nDstAngleY ( ) + nDeltaY * 90 )
	
--------------------------------------------------------------------------------
end
