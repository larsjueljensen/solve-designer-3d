function MovingObjectLocalAI.logModel ( sMsg, hObject )
--------------------------------------------------------------------------------
	
    if ( hObject ~= nil ) then
        log.message ( sMsg.." Model: "..object.getModelName ( hObject ) )
    else
        log.message ( sMsg.." Model: Object is NIL" )
    end

--------------------------------------------------------------------------------
end
