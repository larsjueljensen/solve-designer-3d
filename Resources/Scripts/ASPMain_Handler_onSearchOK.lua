function ASPMain.onSearchOK (  )
--------------------------------------------------------------------------------
	
    log.message ( "SearchOK" )
    
	if this.SearchMode ( ) == 1 then
    
        this.ReceiveSearchSeries ( )
    
    elseif this.SearchMode ( ) == 2 then
    
        this.ReceiveSearchArticle ( )
    
    end
	
--------------------------------------------------------------------------------
end
