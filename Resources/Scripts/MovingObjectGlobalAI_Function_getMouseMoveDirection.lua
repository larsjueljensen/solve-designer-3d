function MovingObjectGlobalAI.getMouseMoveDirection ( x, y, z )
--------------------------------------------------------------------------------

    local px, py, pz = object.getTranslation ( this.getObject ( ), object.kGlobalSpace )    
    return x - px, y - py, z - pz
    
	
--------------------------------------------------------------------------------
end
